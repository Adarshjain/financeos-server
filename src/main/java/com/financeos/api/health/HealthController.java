package com.financeos.api.health;

import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import java.time.Instant;
import java.util.HashMap;
import java.util.Map;

@RestController
@RequestMapping("/api/v1/health")
public class HealthController {

    @GetMapping("/ping")
    public Map<String, Object> ping() {
        Map<String, Object> response = new HashMap<>();
        response.put("status", "OK");
        response.put("message", "FinanceOS API is running");
        response.put("timestamp", Instant.now().toString());
        response.put("version", "1.0.0");
        return response;
    }

    @GetMapping("/info")
    public Map<String, Object> info() {
        Map<String, Object> response = new HashMap<>();
        response.put("application", "FinanceOS Backend");
        response.put("database", "Oracle Autonomous Database");
        response.put("deployment", "Oracle Cloud Infrastructure");
        response.put("timestamp", Instant.now().toString());
        return response;
    }
}
